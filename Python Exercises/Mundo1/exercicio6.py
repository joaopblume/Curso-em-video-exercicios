# Crie um algorítmo que leia um número e
#  mostre o seu dobro, triplo e raíz quadrada.

num = int(input('Digite um número: '))
dobro = num * 2
triplo = num * 3
raiz = num ** (1/2)
print(f'Você escolheu o número {num}, seu dobro é igual a {dobro},'
      f' seu triplo {triplo} e sua raiz quadrada {raiz}.')
 